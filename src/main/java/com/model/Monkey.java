package com.model;

public class Monkey {
	
	int x;
	int y;
	float z;
	String name;
	
	public Monkey() {
	}
	
	public Monkey(int x, int y, float z, String name) {
		super();
		this.x = x;
		this.y = y;
		this.z = z;
		this.name = name;
	}

	public int getX() {
		return x;
	}
	
	public void setX(int x) {
		this.x = x;
	}
	
	public int getY() {
		return y;
	}
	
	public void setY(int y) {
		this.y = y;
	}
	
	public float getZ() {
		return z;
	}
	
	public void setZ(float z) {
		this.z = z;
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	

}
